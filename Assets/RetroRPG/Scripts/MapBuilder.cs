    // Start is called before the first frame update
    void Start()
    {
        StartCoroutine(BuildMap());
    }

    IEnumerator BuildMap()
    {
        for (var x = 0; x < 35; x++)
        {
            for (var y = 0; y < 35; y++)
            {
                if (_walls[x, y] == 2)
                {
                    //Player.transform.position = new Vector3(x, 0.5f, y);
                }

                if (_walls[x, y] == 1)
                {
                    var wall = Instantiate(WallPrefab);
                    wall.transform.position = new Vector3(x, 0.5f, y);
                    wall.transform.parent = transform;
                } else if (_walls[x, y] == 4)
                {
                    var door = Instantiate(DoorPrefab);
                    door.transform.position = new Vector3(x, 0.5f, y);
                    door.transform.parent = transform;
                    door.gameObject.AddComponent<SlidingDoor>();
                }
            }
        }
        
        yield return null;
    }

